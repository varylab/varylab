package de.varylab.varylab.plugin.blender.data;

public class BlenderMeshException extends Exception {

	private static final long 
		serialVersionUID = -6135477422369850811L;

	public BlenderMeshException() {
	}

	public BlenderMeshException(String message) {
		super(message);
	}

	public BlenderMeshException(Throwable cause) {
		super(cause);
	}

	public BlenderMeshException(String message, Throwable cause) {
		super(message, cause);
	}

}
