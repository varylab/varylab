package de.varylab.varylab.plugin.grasshopper.data;

public class RVLMeshException extends Exception {

	private static final long 
		serialVersionUID = -6135477422369850811L;

	public RVLMeshException() {
	}

	public RVLMeshException(String message) {
		super(message);
	}

	public RVLMeshException(Throwable cause) {
		super(cause);
	}

	public RVLMeshException(String message, Throwable cause) {
		super(message, cause);
	}

}
