package de.varylab.varylab.plugin.grasshopper.data;

public class RVLSurfaceException extends Exception {

	private static final long 
		serialVersionUID = -6135477422369850811L;

	public RVLSurfaceException() {
	}

	public RVLSurfaceException(String message) {
		super(message);
	}

	public RVLSurfaceException(Throwable cause) {
		super(cause);
	}

	public RVLSurfaceException(String message, Throwable cause) {
		super(message, cause);
	}

}
